from accum import accum
import unittest
from random import randint

def accum_sol6792(s):
    a = list(s)
    res = ""
    for i, c in enumerate(a):
        res += c * (i + 1) + "-"
    return res.strip("-").title()

def do_ex():
    i = 0
    res = ""
    while (i < 11):
        if (i % 5 == 0):
            n = randint(65, 90) 
        else:
            n = randint(97, 122)
        res += chr(n)
        i += 1
    return res

